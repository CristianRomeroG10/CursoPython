# suma  y resta (+ y -)

suma = 12 + 5
resta = 12 - 5

#multiplicacion y division (* y /)

multiplicacion = 12 * 5
division = 12 / 5 #devuelve un dato float (flotante)

#potencia (exponente) (**)

exponente = 12 ** 5

#division baja (//)
division_baja = 12 // 5 # devuelve entero redondeado hacia abajo

#resto o modulo
resto = 12 % 5

#type(dato) nos devuelve que tipo de dato es
tipo_de_dato = type(5) 

print(f"la suma de 12 + 5 = {suma}")
print(f"la resta de 12 - 5 = {resta}")
print(f"la resta de 12 * 5 = {multiplicacion}")
print(f"la division de 12 / 5 ={division}")
print(f"el resultado de elevar 12 a la 5 = {exponente}")
print(f"division baja de 12//5 = {division_baja}")
print(f"El modulo de 12 % 5 ={resto}")
print(tipo_de_dato)


